#include <stdio.h>

int main()
{
    // An integer variable num that will contain 10, c language that prints an integer number
    int m = 10;
    printf("Number is %d\n", m);

    int num1 = 20, num2 = 30;
    printf("Number is %d %d\n", num1, num2);

    // Write a program that prints a floating, double and character

    float one = 10.5;
    double two = 10.525555554;
    char ch = 'a';

    printf("one = %f\n", one);
    printf("two = %lf\n", two);
    printf("ch = %c\n", ch);
    
    return 0;
}